function largestOfFour(arr) {
   let largest = [];
   for (let i = 0; i < arr.length; i++) {
     for (let j = 0; j < arr[j]; j++) {
       var largestNumber = arr.reduce(arr[i]);
        return largest.push(largestNumber);
     }
   }
    return largest;
  }

